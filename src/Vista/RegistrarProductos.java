    private void jButtonGuardarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarProductoActionPerformed

        try {
            Productos p1 = new Productos();

            if (jTextFieldNombreProducto.getText().equalsIgnoreCase("")) {
                throw new NullPointerException("El campo nombre no puede estar vacio");
            }
            if (jTextAreaDesciripcionProducto.getText().equalsIgnoreCase("")) {
                throw new NullPointerException("El campo descripcion no puede estar vacio");
            }
            p1.setNombreProducto(jTextFieldNombreProducto.getText());
            p1.setDescripcionProducto(jTextAreaDesciripcionProducto.getText()); 
            p1.setCreatedAt(new Date());
            p1.setEstado("A");
            
            productoJpa.create(p1);
            JOptionPane.showMessageDialog(null, "Se ha agregado el producto correctamente");
            jTextAreaDesciripcionProducto.setText("");
            jTextFieldNombreProducto.setText("");
            
        } catch (HeadlessException | NumberFormatException | NullPointerException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonGuardarProductoActionPerformed

