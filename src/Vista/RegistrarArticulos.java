    private void cargarDatosProductos() {

        try {
            List<Productos> listaProductos = null;
            listaProductos = productoJpa.findProductosEntities();
            List<Productos> listaFinal = new ArrayList<>();
            Productos p;
            jComboBoxProductos.addItem("Seleccione");
            for (int i = 0; i < listaProductos.size(); i++) {
                if (listaProductos.get(i).getEstado().equalsIgnoreCase("A")) {
                    p = listaProductos.get(i);
                    listaFinal.add(p);
                }
            }

            for (int i = 0; i < listaFinal.size(); i++) {
                jComboBoxProductos.addItem(listaFinal.get(i).getIdProducto().toString()
                        + "-:-" + listaFinal.get(i).getNombreProducto());

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

