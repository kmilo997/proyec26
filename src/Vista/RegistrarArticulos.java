    private void jButtonRegistrarArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarArticuloActionPerformed

        try {
            
            if (jTextFieldNombreArticulo.getText().equalsIgnoreCase("")) {
                throw new NullPointerException("El campo nombre artículo no puede estar vacio");
            }
            if (jTextAreaDescripcionArticulo.getText().equalsIgnoreCase("")) {
                throw new NullPointerException("El campo descripción artículo no puede estar vacio");
            }
            if (jTextFieldPrecioCompra.getText().equalsIgnoreCase("")) {
                throw new NullPointerException("El campo precio compra no puede estar vacio");
            }
            if (jTextFieldPrecioVenta.getText().equalsIgnoreCase("")) {
                throw new NullPointerException("El campo precio venta no puede estar vacio");
            }
            if (jTextFieldStock.getText().equalsIgnoreCase("")) {
                throw new NullPointerException("El campo stock no puede estar vacio");
            }
            if (jComboBoxProductos.getSelectedIndex() == 0) {
                throw new NullPointerException("Selecciona un producto");
            }

            Articulos a1 = new Articulos();
            a1.setCreatedAt(new Date());
            a1.setNombreArticulo(jTextFieldNombreArticulo.getText());
            a1.setDescripcionArticulo(jTextAreaDescripcionArticulo.getText());
            a1.setValorCompra(Integer.parseInt(jTextFieldPrecioCompra.getText()));
            a1.setValorVenta(Integer.parseInt(jTextFieldPrecioVenta.getText()));
            a1.setStockArticulo(Integer.parseInt(jTextFieldStock.getText()));
            int pv = Integer.parseInt(jTextFieldPrecioVenta.getText());
            int pc = Integer.parseInt(jTextFieldPrecioCompra.getText());
            
            int ganancia = pv - pc; 
                    
            a1.setPorcentajeGanacia(ganancia);
            a1.setIdProducto(
                    productoJpa.findProductos(Long.parseLong(jComboBoxProductos.getSelectedItem().toString().split("-:-")[0])));
            a1.setCodigoBarras(jTextFieldCodigoBarras.getText());
            articuloJpa.create(a1);
            JOptionPane.showMessageDialog(null, "Se ha registrado correctamente");

            jComboBoxProductos.setSelectedIndex(0);
            jTextAreaDescripcionArticulo.setText("");
            jTextFieldCodigoBarras.setText("");
            jTextFieldGanancia.setText("");
            jTextFieldNombreArticulo.setText("");
            jTextFieldPrecioCompra.setText("");
            jTextFieldPrecioVenta.setText("");
            jTextFieldStock.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


        
    }//GEN-LAST:event_jButtonRegistrarArticuloActionPerformed

