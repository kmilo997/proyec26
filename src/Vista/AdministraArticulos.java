    private void CrearModelo() {
        try {

            modelo = (new DefaultTableModel(
                    null, new String[]{
                        "Id", "Producto", "Artículo", "Precio", "Stock", "Opciones", "Opciones"
                    }) {
                Class[] types = new Class[]{
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class,
                    javax.swing.JButton.class,
                    javax.swing.JButton.class
                };
                boolean[] canEdit = new boolean[]{
                    false, false, false, false, false, false, false
                };

                @Override
                public Class getColumnClass(int columnIndex) {
                    return types[columnIndex];
                }

                @Override
                public boolean isCellEditable(int rowIndex, int colIndex) {
                    return canEdit[colIndex];
                }
            });
            jTableArticulo.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString() + "error2");
        }
    }

    private void cargaInformacionArticulo() {
        try {
            Object o[] = null;
            List<Articulos> listaArticulos = articuloJpa.findArticulosEntities();
            jTableArticulo.setDefaultRenderer(Object.class, new Render());
            jTableArticulo.setRowHeight(30);
            jButtonEditar.setName("editar");
            jButtonEliminar.setName("eliminar");
            List<Articulos> listaFinal = new ArrayList<>();
            Articulos a;
            for (int i = 0; i < listaArticulos.size(); i++) {
                    listaFinal.add(listaArticulos.get(i));            
            }

            for (int i = 0; i < listaFinal.size(); i++) {
                modelo.addRow(o);
                modelo.setValueAt(listaFinal.get(i).getIdArticulo(), i, 0);
                modelo.setValueAt(listaFinal.get(i).getIdProducto().getNombreProducto(), i, 1);
                modelo.setValueAt(listaFinal.get(i).getNombreArticulo(), i, 2);
                modelo.setValueAt(listaFinal.get(i).getValorCompra(), i, 3);
                modelo.setValueAt(listaFinal.get(i).getStockArticulo(), i, 4);
                modelo.setValueAt(jButtonEditar, i, 5);
                modelo.setValueAt(jButtonEliminar, i, 6);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage() + "Eoorro");
        }
    }

