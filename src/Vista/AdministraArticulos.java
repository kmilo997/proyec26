    private void jTableArticuloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableArticuloMouseClicked

        DefaultTableModel tm = (DefaultTableModel) jTableArticulo.getModel();
        String dato = String.valueOf(tm.getValueAt(jTableArticulo.getSelectedRow(), 0));
        Articulos a = articuloJpa.findArticulos(Long.parseLong(dato));

        System.out.println(a);

        int column = jTableArticulo.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / jTableArticulo.getRowHeight();

        if (row < jTableArticulo.getRowCount() && row >= 0 && column < jTableArticulo.getColumnCount() && column >= 0) {
            Object value = jTableArticulo.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;
                if (boton.getName().equalsIgnoreCase("editar")) {

                    EditarArticulos a1 = new EditarArticulos();
                    a1.setVisible(true);
                    a1.setIconifiable(true);
                    a1.setMaximizable(true);
                    a1.setClosable(true);
                    a1.setResizable(true);
                    jdesktop.add(a1);
                    dispose();
                } else {

                    int opc = JOptionPane.showConfirmDialog(null,
                            "Desea Eliminar el artículo " + a.getNombreArticulo(), "CONFIRMACIÓN", JOptionPane.YES_NO_OPTION);
                    if (opc == 0) {

                        try {
                            a.setEstado("B");
                            a.setDeletedAt(new Date());
                            articuloJpa.edit(a);
                            CrearModelo();
                            cargaInformacionArticulo();
                        } catch (NonexistentEntityException ex) {
                            Logger.getLogger(AdministrarProductos.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (Exception ex) {
                            Logger.getLogger(AdministrarProductos.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }

                }
            }

        }

    }//GEN-LAST:event_jTableArticuloMouseClicked

